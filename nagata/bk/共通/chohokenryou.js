function validateInput1(inputId, inputValue) {
    // 正規表現で数字のみを検証
    var isNumeric = /^[0-9]+$/.test(inputValue);

    // 数字以外が含まれている場合
    if (!isNumeric) {
        alert('半角数字を入力してください。');
    } else {
        // 数字の範囲を検証
        var numberValue = parseInt(inputValue, 10);
        if (numberValue < 1 || numberValue > 99999999999) {
            alert('1から99999999999の範囲で入力してください。');
        }
    }
}



